CREATE TABLE CASH_CLOSURE (
    ID INT PRIMARY KEY AUTO_INCREMENT,
    DATE DATE NOT NULL UNIQUE,
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    BILL_2_COUNT INT NOT NULL,
    BILL_5_COUNT INT NOT NULL,
    BILL_10_COUNT INT NOT NULL,
    BILL_20_COUNT INT NOT NULL,
    BILL_50_COUNT INT NOT NULL,
    BILL_100_COUNT INT NOT NULL,
    BILL_200_COUNT INT NOT NULL,
    COIN_1_COUNT INT NOT NULL,
    COIN_5_COUNT INT NOT NULL,
    COIN_10_COUNT INT NOT NULL,
    COIN_25_COUNT INT NOT NULL,
    COIN_50_COUNT INT NOT NULL,
    COIN_1_REAL_COUNT INT NOT NULL,
    CASH_REGISTER_WITHDRAWAL DECIMAL(10, 2) NOT NULL
);
